// The concat() method is used to merge two or more arrays.
// This method does not change the existing arrays, but instead returns a new array.

// Example:
let courses = ["Mobile", "programming", "basic electronics"];
let more_courses = ["business", "arts", "community health"];

let allCourses = courses.concat(more_courses);

console.log(allCourses); //Output: [ 'Mobile','programming','basic electronics','business','arts','community health' ]
