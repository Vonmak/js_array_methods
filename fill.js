// the fill() method fills all the elements of an array from start index to an end index with static values.
// It can take upto three arguments;
// 1. value to fill with,
// 2. start index, and
// 3. end index.

// Example:
const arr=['gray', 'yellow', 'orange', 'pink'];
arr.fill('green',2,3);
console.log(arr); // Outpu: ['gray', 'yellow', 'green', 'pink']